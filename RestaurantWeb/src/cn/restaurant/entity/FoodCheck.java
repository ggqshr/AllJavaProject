package cn.restaurant.entity;

public class FoodCheck {
private int checkNumber;
private String businessNumber;
private String waiterNumber;
private String foodNumber;
private int foodNum;
private float checkPrice;
private String checkDate;

public int getCheckNumber() {
	return checkNumber;
}
public void setCheckNumber(int checkNumber) {
	this.checkNumber = checkNumber;
}
public String getBusinessNumber() {
	return businessNumber;
}
public void setBusinessNumber(String businessNumber) {
	this.businessNumber = businessNumber;
}
public String getWaiterNumber() {
	return waiterNumber;
}
public void setWaiterNumber(String waiterNumber) {
	this.waiterNumber = waiterNumber;
}
public String getFoodNumber() {
	return foodNumber;
}
public void setFoodNumber(String foodNumber) {
	this.foodNumber = foodNumber;
}
public int getFoodNum() {
	return foodNum;
}
public void setFoodNum(int foodNum) {
	this.foodNum = foodNum;
}
public float getCheckPrice() {
	return checkPrice;
}
public void setCheckPrice(float checkPrice) {
	this.checkPrice = checkPrice;
}
public String getCheckDate() {
	return checkDate;
}
public void setCheckDate(String checkDate) {
	this.checkDate = checkDate;
}

}
